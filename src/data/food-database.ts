// ─────────────────────────────────────────────────────────────
// Búsqueda de alimentos con normalización de acentos
// ─────────────────────────────────────────────────────────────
export function searchFoods(query: string): FoodItem[] {
  const q = query.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
  return FOOD_DATABASE.filter(f =>
    f.name.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '').includes(q) ||
    f.tags.some(t => t.includes(q))
  ).slice(0, 8);
}

// Obtener alimentos por categoría
export function getFoodsByCategory(category: FoodItem['category']): FoodItem[] {
  return FOOD_DATABASE.filter(f => f.category === category);
}

// Calcular nutrientes para una cantidad dada en gramos
export function calculateNutrients(food: FoodItem, grams: number) {
  const factor = grams / 100;
  return {
    calories: Math.round(food.per100g.calories * factor),
    protein: Math.round(food.per100g.protein * factor * 10) / 10,
    carbs: Math.round(food.per100g.carbs * factor * 10) / 10,
    fat: Math.round(food.per100g.fat * factor * 10) / 10,
    fiber: Math.round(food.per100g.fiber * factor * 10) / 10,
  };
}
